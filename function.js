/*============ Function 2 Number Additon & 2 Number Multiplation ===========*/
function doubleIt(num){
    var result = num * 2;
    return result;
}
var first = doubleIt(5);
var second = doubleIt(10);
var total = first + second;
console.log(total);

/*=========== Function Additional With 2 Number ===========*/
function add(num1,num2){
    var result = num1 + num2;
    return result;
}

var sum = add(15,17);
console.log(sum);